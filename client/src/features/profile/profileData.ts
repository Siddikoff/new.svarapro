/**
 * Builds the `data:` URL for the dropdown chevron icon. Theme-aware so the
 * arrow stays legible on both light and dark backgrounds.
 */
export function buildChevronUrl(theme: 'light' | 'dark' | string): string {
  const stroke = theme === 'light' ? '%231a2533' : '%23ffffff';
  return (
    "url(\"data:image/svg+xml;utf8,<svg xmlns='http://www.w3.org/2000/svg' " +
    "width='10' height='14' viewBox='0 0 10 14' fill='none' stroke='" +
    stroke +
    "' stroke-width='1.5' stroke-linecap='round' stroke-linejoin='round'>" +
    "<polyline points='2,5 5,2 8,5'/><polyline points='2,9 5,12 8,9'/></svg>\")"
  );
}
